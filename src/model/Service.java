package model;

import java.io.Serializable;
import java.util.Date;

/**
 *
 * @author datnvt
 */
public class Service implements Serializable {
    private int id;
    private String name;
    private float price;
    private String des; 
    private String time;
    
    public Service() {
        super();
    }

    public Service(int id, String name, float price, String des, String time) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.des = des;
        this.time = time;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }

    public String getDes() {
        return des;
    }

    public void setDes(String des) {
        this.des = des;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
